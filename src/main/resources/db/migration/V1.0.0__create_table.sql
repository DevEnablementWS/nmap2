CREATE TABLE IF NOT EXISTS toilets
(
    id       BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name     VARCHAR(255),
    address  VARCHAR(255),
    maker_id BIGINT,
    CONSTRAINT pk_toilets PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS makers
(
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    company_name VARCHAR(255),
    CONSTRAINT pk_makers PRIMARY KEY (id)
);

ALTER TABLE toilets
    ADD CONSTRAINT FK_TOILETS_ON_MAKER FOREIGN KEY (maker_id) REFERENCES makers (id);
